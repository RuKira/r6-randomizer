export function refreshOps(setters) {
    setters.setChosenAttackers([]);
    setters.setChosenDefenders([]);
    setters.setLockedAttackers([]);
    setters.setLockedDefenders([]);
    setters.setRerolledAttackers([]);
    setters.setRerolledDefenders([]);
    setters.setPlayedAttackers([]);
    setters.setPlayedDefenders([]);
}
